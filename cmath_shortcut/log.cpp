#include<bits/stdc++.h>

using namespace std;

int main () {

    float x;
    cout << "Enter a number: " ;
    cin >> x;

    cout << "log(" << x << ")   = " << log(x) << endl;
    cout << "log2(" << x << ")  = " << log2(x) << endl;
    cout << "log10(" << x << ") = " << log10(x) << endl;

    return 0;
}